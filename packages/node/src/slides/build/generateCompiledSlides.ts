import { createHash } from "node:crypto";
import { mkdir, readdir, readFile, unlink, writeFile } from "node:fs/promises";
import path from "node:path";
import { compile } from "@mdx-js/mdx";
import type { Plugin, ViteDevServer } from "vite";
import { getMdxCompileOptions } from "../compiling/mdx-options.ts";
import { parseFrontmatter } from "../parsing/frontmatter.ts";
import { parseSlides, parseSlideMeta } from "../parsing/parseSlides.ts";
import type { SlideUnit } from "../model/slide.ts";
import { validateSlidesAuthoring } from "../validation/validateSlidesAuthoring.ts";

const GENERATED_SLIDES_DIR_ROOT = ".generated/slides";
const GENERATED_SLIDE_MODULES_DIR = `${GENERATED_SLIDES_DIR_ROOT}/slides`;
const GENERATED_MANIFEST_FILE = `${GENERATED_SLIDES_DIR_ROOT}/index.ts`;

function hashString(source: string) {
  return createHash("sha256").update(source).digest("hex").slice(0, 12);
}

function toModuleIdentifier(slideId: string) {
  return slideId.replace(/[^a-zA-Z0-9_$]/g, "_");
}

function createSlideModuleFileName(index: number) {
  return `slide-${index + 1}.tsx`;
}

function createSlideModuleCode(compiledSource: string) {
  return [
    "/* eslint-disable */",
    "/* @generated by compile-time slides pipeline */",
    compiledSource.trim(),
    "",
  ].join("\n");
}

function normalizeDiagramCodeProps(compiledSource: string) {
  return compiledSource.replace(
    /<(MermaidDiagram|PlantUmlDiagram) code="([\s\S]*?)" \/>/g,
    (_match, componentName: string, code: string) =>
      `<${componentName} code={${JSON.stringify(code)}} />`,
  );
}

function createManifestCode({
  deckSourceHash,
  deckMeta,
  slideEntries,
}: {
  deckSourceHash: string;
  deckMeta: Record<string, unknown>;
  slideEntries: Array<{
    id: string;
    importName: string;
    importPath: string;
    meta: Record<string, unknown>;
  }>;
}) {
  const imports = slideEntries
    .map((entry) => `import ${entry.importName} from '${entry.importPath}'`)
    .join("\n");
  const slides = slideEntries
    .map(
      (entry) => `    {
      id: ${JSON.stringify(entry.id)},
      meta: ${JSON.stringify(entry.meta, null, 6)},
      component: ${entry.importName},
    }`,
    )
    .join(",\n");

  return [
    "/* eslint-disable */",
    "/* @generated by compile-time slides pipeline */",
    "import type { CompiledSlidesManifest } from '@slidev-react/node/slides/model/compiled-slides'",
    imports,
    "",
    "const compiledSlides: CompiledSlidesManifest = {",
    `  sourceHash: ${JSON.stringify(deckSourceHash)},`,
    `  meta: ${JSON.stringify(deckMeta, null, 2)},`,
    "  slides: [",
    slides,
    "  ],",
    "}",
    "",
    "export default compiledSlides",
    "",
  ].join("\n");
}

async function writeIfChanged(filePath: string, content: string) {
  try {
    const current = await readFile(filePath, "utf8");
    if (current === content) return;
  } catch {
    // File does not exist yet.
  }

  await mkdir(path.dirname(filePath), { recursive: true });
  await writeFile(filePath, content, "utf8");
}

async function removeStaleGeneratedSlides(slidesDir: string, expectedFiles: Set<string>) {
  try {
    const files = await readdir(slidesDir);
    await Promise.all(
      files.map(async (fileName) => {
        if (!expectedFiles.has(fileName)) await unlink(path.join(slidesDir, fileName));
      }),
    );
  } catch {
    // Directory does not exist yet.
  }
}

async function compileSlideModule(source: string) {
  const compiled = await compile(source, {
    ...getMdxCompileOptions(),
    development: false,
    jsx: true,
    jsxImportSource: "react",
    providerImportSource: "@mdx-js/react",
  });

  return normalizeDiagramCodeProps(String(compiled));
}

function createSlidesSourceHash({
  slidesSource,
  resolvedSlides,
}: {
  slidesSource: string;
  resolvedSlides: Array<{
    id: string;
    source: string;
    externalFilePath?: string;
    externalFileSource?: string;
  }>;
}) {
  return hashString(
    JSON.stringify({
      slidesSource,
      slides: resolvedSlides.map((slide) => ({
        id: slide.id,
        source: slide.source,
        externalFilePath: slide.externalFilePath,
        externalFileSource: slide.externalFileSource,
      })),
    }),
  );
}

async function resolveSlideUnitSource({
  slide,
  slidesSourceFile,
}: {
  slide: SlideUnit;
  slidesSourceFile: string;
}) {
  const slideSrc = slide.meta.src?.trim();
  if (!slideSrc) {
    return {
      ...slide,
      watchedFiles: [] as string[],
      externalFilePath: undefined,
      externalFileSource: undefined,
    };
  }

  if (slide.hasInlineSource) {
    throw new Error(
      `Slide ${slide.index + 1}${slide.meta.title ? ` (${slide.meta.title})` : ""} mixes inline content with src. Use one or the other.`,
    );
  }

  const externalFilePath = path.resolve(path.dirname(slidesSourceFile), slideSrc);
  let externalFileSource: string;

  try {
    externalFileSource = await readFile(externalFilePath, "utf8");
  } catch (error) {
    throw new Error(
      `Failed to load slide ${slide.index + 1}${slide.meta.title ? ` (${slide.meta.title})` : ""} src "${slideSrc}": ${error instanceof Error ? error.message : String(error)}`,
      { cause: error },
    );
  }

  const externalMatter = parseFrontmatter(externalFileSource.replace(/\r\n/g, "\n").trim());
  const externalMeta = parseSlideMeta(
    externalMatter.data,
    `slide ${slide.index + 1} src "${slideSrc}"`,
  );

  if (externalMeta.src) {
    throw new Error(
      `Nested src is not supported in slide ${slide.index + 1}${slide.meta.title ? ` (${slide.meta.title})` : ""}.`,
    );
  }

  const mergedMeta = {
    ...externalMeta,
    ...slide.meta,
  };
  const resolvedSource = externalMatter.content.trim() || "# Empty slide";

  return {
    ...slide,
    meta: mergedMeta,
    source: resolvedSource,
    watchedFiles: [externalFilePath],
    externalFilePath,
    externalFileSource,
  };
}

export async function generateCompiledSlidesArtifacts(options: {
  appRoot: string;
  slidesSourceFile: string;
}) {
  const { appRoot, slidesSourceFile } = options;
  const slidesSource = await readFile(slidesSourceFile, "utf8");
  const parsedSlides = parseSlides(slidesSource);
  const warnings = await validateSlidesAuthoring({
    appRoot,
    slides: parsedSlides,
  });
  const generatedSlidesRootDir = path.join(appRoot, GENERATED_SLIDES_DIR_ROOT);
  const generatedSlideModulesDir = path.join(appRoot, GENERATED_SLIDE_MODULES_DIR);
  const manifestFile = path.join(appRoot, GENERATED_MANIFEST_FILE);
  const expectedSlideFiles = new Set<string>();
  const watchedFiles = new Set<string>([slidesSourceFile]);
  const manifestEntries: Array<{
    id: string;
    importName: string;
    importPath: string;
    meta: Record<string, unknown>;
  }> = [];
  const resolvedSlides = await Promise.all(
    parsedSlides.slides.map((slide) =>
      resolveSlideUnitSource({
        slide,
        slidesSourceFile,
      }),
    ),
  );
  const sourceHash = createSlidesSourceHash({
    slidesSource,
    resolvedSlides,
  });

  await mkdir(generatedSlideModulesDir, { recursive: true });

  for (const slide of resolvedSlides) {
    const slideFileName = createSlideModuleFileName(slide.index);
    const slideFilePath = path.join(generatedSlideModulesDir, slideFileName);
    const importName = toModuleIdentifier(slide.id);

    for (const watchedFile of slide.watchedFiles) watchedFiles.add(watchedFile);

    try {
      const compiledSource = await compileSlideModule(slide.source);
      await writeIfChanged(slideFilePath, createSlideModuleCode(compiledSource));
    } catch (error) {
      const slideName = slide.meta.title
        ? `${slide.index + 1} (${slide.meta.title})`
        : String(slide.index + 1);
      throw new Error(
        `Failed to compile slide ${slideName}: ${error instanceof Error ? error.message : String(error)}`,
        {
          cause: error,
        },
      );
    }

    expectedSlideFiles.add(slideFileName);
    manifestEntries.push({
      id: slide.id,
      importName,
      importPath: `./slides/${path.basename(slideFileName, path.extname(slideFileName))}`,
      meta: slide.meta,
    });
  }

  await removeStaleGeneratedSlides(generatedSlideModulesDir, expectedSlideFiles);
  await writeIfChanged(
    manifestFile,
    createManifestCode({
      deckSourceHash: sourceHash,
      deckMeta: parsedSlides.meta,
      slideEntries: manifestEntries,
    }),
  );

  return {
    generatedSlidesRootDir,
    manifestFile,
    sourceHash,
    watchedFiles: [...watchedFiles],
    warnings,
  };
}

function createSlidesGenerator(options: { appRoot: string; slidesSourceFile: string }) {
  let generatePromise: Promise<void> | null = null;
  let rerunRequested = false;
  let watchedFiles = new Set<string>([options.slidesSourceFile]);
  let warnings: string[] = [];

  const generate = async () => {
    if (generatePromise) {
      rerunRequested = true;
      await generatePromise;
      return;
    }

    generatePromise = (async () => {
      do {
        rerunRequested = false;
        const result = await generateCompiledSlidesArtifacts(options);
        watchedFiles = new Set(result.watchedFiles);
        warnings = result.warnings;
      } while (rerunRequested);
    })();

    try {
      await generatePromise;
    } finally {
      generatePromise = null;
    }
  };

  return {
    generate,
    getWatchedFiles: () => [...watchedFiles],
    getWarnings: () => warnings,
  };
}

async function regenerateSlidesForServer(server: ViteDevServer, generate: () => Promise<void>) {
  try {
    await generate();
    server.ws.send({ type: "full-reload" });
  } catch (error) {
    server.config.logger.error(
      error instanceof Error ? (error.stack ?? error.message) : String(error),
      { timestamp: true },
    );
  }
}

export function pluginCompileTimeSlides(options: {
  appRoot: string;
  slidesSourceFile: string;
}): Plugin {
  const slidesSourceFile = path.resolve(options.slidesSourceFile);
  const generator = createSlidesGenerator({
    ...options,
    slidesSourceFile,
  });

  return {
    name: "slide-react-compile-time-slides",
    async buildStart() {
      await generator.generate();
      for (const warning of generator.getWarnings()) {
        this.warn(warning);
      }
      for (const watchedFile of generator.getWatchedFiles()) {
        this.addWatchFile(watchedFile);
      }
    },
    configureServer(server) {
      server.watcher.add(slidesSourceFile);

      const syncWatchedFiles = () => {
        server.watcher.add(generator.getWatchedFiles());
      };

      const handleSlidesChange = async (filePath: string) => {
        const resolvedFilePath = path.resolve(filePath);
        if (!generator.getWatchedFiles().includes(resolvedFilePath)) return;

        await regenerateSlidesForServer(server, async () => {
          await generator.generate();
          for (const warning of generator.getWarnings()) {
            server.config.logger.warn(warning, { timestamp: true });
          }
          syncWatchedFiles();
        });
      };

      syncWatchedFiles();
      server.watcher.on("add", handleSlidesChange);
      server.watcher.on("change", handleSlidesChange);
      server.watcher.on("unlink", handleSlidesChange);
    },
  };
}

